                    <p>A new flagship project, namely 'Lokprasar Prakalpo', has been introduced by the Department of Information and Cultural Affairs with the inspiration of Hon'ble Chief Minister Mamata Banerjee. The main objectives of the project are: (I) to bestow dignity on folk artists by issuing them identity cards; (2) to uphold the traditional folk forms of Bengal; (3) to revive nearly-extinct folk forms of art and culture; and (4) to uplift the socioeconomic conditions of artists associated with folk culture.
					</p>
					
                    <p>The Department of Information and Cultural Affairs has been implementing the following programs under 'Lokprasar Prakalpo':</p>
					
                    <ul>
					<li>Issuing an identity card to each folk artist.</li>
					<li>Arranging for a monthly pension for veteran and distressed folk artists.</li>
					<li>Granting a retainer fee to artists.</li>
					<li>Arrange for the minimum income by engaging them in the public campaign program on development projects.</li>
                    </ul>
                    <h4>Issuing of Identity Cards :</h4>
                    <p>
					One of the many objectives of the 'Lokprasar Prakalpo' is to bestow dignity on every folk artist. Unfortunately, folk artists hardly get their due respect, and they remain ignored in society. The government has taken the initiative to confer upon them their due honor through LokprasarPrakalpo. So far, 84,720 artists have been provided with identity cards across the state.

					</p>
						
                    <h4>Provisions for pensions for the needy and elderly folk artistes :</h4>
                    <p>
					There are many folk artists in Bengal who used to perform in various programs with a reputation that attracted the attention of audiences. Most of them are now unable to perform because of their old age. They don't have a definite source of income, and there is nobody to look after them. The government helps them and provides social security for them through Lokprasar Prakalpo. The folk artists who are above 60 years of age are getting pensions under LokprasarPrakalpo. At present, in West Bengal, about 8,596 artists have been receiving Rs. 1000 per month.

					</p>

                    <h4>Retainer fee for the artistes :</h4>
                    <p>
					The enlisted folk artists under the age of 60 years in Lokprasar Prakalpo who are engaged in publicity campaigns in different departments have been receiving Rs. 1000 per month as retainer fees. Till now, approximately 1,94,300 folk artists have been receiving the retainer fee.

					</p>

                    <h4>Utilisation of services of the Folk Artistes for the publicity of Development Projects :</h4>
					
                    <p>
					The government is on the mission of publicizing its flagship program through Lokprasar Prakalpo among the people. For instance, the folk artists are being utilized for disseminating information regarding schemes like Kannyasree, Yubasree, Sikshasree, etc. and programs like Nirmal Bangla, Sarbo Siksha Avijan, etc. to the common people. Initiative has been taken for arranging 4â€“5 numbers of programs for each artist. A yearlong campaign is organized through mobile Tableau Vans, where folk artists are engaged by rotation in each district and sub-division.

					</p>
					
					<p>
					An initiative has been taken to organize workshops in each district with the assistance of the researchers on folk culture to upgrade the traditional folk forms for presentation to new generations in a more attractive way.

					</p>
					
					<p>
					Lokprasar Prakalpo has already achieved great success through its implementation. The so-called neglected rural folk artists of Bengal are taking part in this scheme with great enthusiasm.

					</p>


