                    <p>
					The scheme “State Welfare Scheme for Purohits” was launched by the Department of Information & Cultural Affairs, West Bengal in September 2020 to benefit the financially distressed practicing temple or tribal purohit and priests of other communities like Christians, Jain, and Buddhists.

					</p>
					
                    <p>
					The Purohits and priests of other communities like Christians, Jains, Buddhists and Parsis perform important social, cultural, and community functions having wide public ramifications. Considering the very significant roles played by the Purohits and priests of other communities like Christians, Jains, Buddhists and Parsis in our society and in view of their financial condition, the State Government has launched the scheme.

					</p>
                    <h4>Benefits :</h4>
                    <ul>
                        <li>
						Monthly financial assistance for all the poor Purohits and priests of other communities like Christians, Jains, Buddhists and Parsis with limited income of ₹1,000/- per month.

						</li>
                        <li>	
						A grant of ₹1,20,000/- will be provided to each poor Purohits or priests of other communities like Christians, Jains, Buddhists or Parsis of lower income for the construction of a dwelling house.

						</li>
                    </ul>
                    <h4>Eligibility :</h4>
                    <ul>
                        <li>
						All the poor Purohits or priests of other communities like Christians, Jains, Buddhists, and Parsis in the State with limited income will be eligible under the scheme.
						</li>
						
                        <li>
						A person who is a resident of West Bengal on the date of making an application under the Scheme will be eligible. 

						</li>
						
                        <li>
						A person is not a beneficiary under any other Social Security Pension Scheme or a recipient of Government pension or pension from any other organization.

						</li>
						
                        <li>
						For receiving a housing grant, a person should not have a pucca dwelling house of his or her own but shall have a piece of land in his or her name. In case the beneficiary does not own land for the construction of the dwelling house, the State Government will mobilize vested and other lands whenever possible for providing housing facilities to such families.
						</li>
						
                        <li>
						An applicant must be a financially distressed or economically backwards practicing priest attached to any place of worship.
						</li>
                    </ul>
                    <h4>How to apply :</h4>
                    <ul>
                        <h6><strong>Step 01: </strong></h6>
                        <li>
						The interested beneficiary may apply under the scheme by filling out the application form in the prescribed format (As per the guidelines of the scheme)<a href="https://wb.gov.in/pdf/state_welfare_scheme.pdf" target="_blank">Click Here</a>
						</li>
                        <h6><strong>Step 02: Application form shall be submitted to:</strong></h6>
                        <li>The Block Development Officer in case the applicant residing in the rural areas
</li>  

                        <li>
						The Sub-Divisional Officer in case the applicant is residing in Municipal or Notified Areas outside the areas of Kolkata Municipal Corporation and to the Municipal Commissioner, Kolkata Municipal Corporation in case the applicant is residing in Kolkata Municipal Corporation area.

						</li> 
						
                        <h6><strong>Step 03: Verification of Application:</strong></h6>
                        <li>
						All pension applications received by the concerned authorities shall be verified with respect to the eligibility criteria.

						</li>
						
                        <li>
						The supporting document, for residential status is to be provided. For housing grants, supporting documents for land ownership, if any are to be provided.

						</li>
						
                        <h6><strong>Step 04: Processing of Application:</strong></h6>
                        <li>
						The application form in the prescribed format in respect of all applicants shall be duly verified by the BDO/SDO, or Municipal Commissioner, KMC, to ensure eligibility under the Scheme.
						</li>
						
                        <li>
						The concerned District Magistrate or Municipal Commissioner, KMC will recommend the names of the eligible persons to the Nodal Department i.e. Department of Information & Cultural Affairs.

						</li>
						
                        <li>
						The Information and Cultural Affairs Department will sanction the financial assistance and effect payment directly to the bank account of the beneficiary.
						</li>
                    </ul>
                    <h4>Documents Required :</h4>
                    <ul>
                        <li>Passport Photograph</li>
                        <li>Copy of Digital Ration Card</li>
                        <li>Copy of Aadhaar Card (if available)</li>
                        <li>Copy of Voter Id</li>
                        <li>Copy of Residential Certificate (Self Declaration)</li>
                        <li>Copy of Bank Pass Book</li>
                        <li>Copy of Khatian (ROR)/ Deed</li>
                    </ul>
                   
