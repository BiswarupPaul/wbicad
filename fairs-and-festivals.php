                            <h2><strong>Fairs and Festivals - </strong></h2>
                            <h4>Kolkata International Film Festival:</h4>
                            <p>The Kolkata International Film Festival (KIFF) is Flagship programme of Government of West Benhgal. Kolkata International Film Festival (KIFF) has become truly international, with its growing profile and signature that has being acknowledged globally. The 29th edition of Kolkata International Film Festival (KIFF) was inaugurated on December 5, 2023 with a colourful inauguration in star studded Netaji Indoor Stadium and continued till December 15th. It gets a participation of more than 200 films including foreign films from different Countries in 23 screening venues. Besides, there was an exhibition on Mrinal Sen and on the memory of Dev Anand to pay tribute to their birth Centinary and the entire event was conducted most successfully.</p>
                            <h4>Durga Puja Carnival (Red Road Carnival):</h4>
                            <p>I & CA Department, Govt. of WB organized “Durga Puja Carnival” on 8th October, 2022 on Red Road, Kolkata and first time at all other Districts on 7th October, 2022 through Colorful tableaux, performance of flamboyant dancers, daredevil performance by Kolkata Police and feet-tapping musical performances by a host of artists. Ninety-five idols of different Puja Committees on 285 vehicles, and more than 5,000 performers, participated in the grand show at Red Road that was attended by over 20,000 spectators, including a huge number of foreign guests and delegates, invited in the wake of the festival being recognized as an intangible heritage by UNESCO. In all the districts, Durga Puja Carnival held for the first time had evoked a huge response among the citizens.</p>
                            <h4>Durga Puja of Kolkata receives UNESCO Heritage Tag:</h4>
                            <p>Durga Puja of Kolkata has been included in UNESCO’s 2021 list of ‘Intangible Cultural Heritage of Humanity’. To commemorate this achievement on 1st September, 2022 a mega rally comprised of around one lakh people led by the Hon’ble Chief Minister, West Bengal from Jorasanko to Red Road was organized followed by a big cultural programme at Red Road where the State Government felicited to UNESCO representatives.</p>
                            <h4>Bangla Sangeet Mela:</h4>
                            <p>‘Bangla Sangeet Mela’ has become a platform to spread the message of unity through rich cultural language and melody of Bengal. ‘Bangla Sangeet Mela’ was held between 25th December 2022 & 1st January 2023, simultaneously at 10 venues located in various places of Kolkata and witnessed participation of about 4,000 artistes, musicians and anchors of West Bengal.</p>
                            <h4>Biswa Bangla Loko-Sanskriti Utsav:</h4>
                            <p>Biswa Bangla Loko-Sanskriti Utsav was organized at Rabindra Sadan-Nandan Complex from 25th December, 2022 to 1st January, 2023. District Level Loksanskriti Festivals were also held.</p>
                            <h4>PoushUtsav:</h4>
                            <p>Poush Utsav was organized during 24th December, 2022 to 31st December, 2022 in three different venues i.e., at Mohar Kunjo, Barisha Club Gound and Tala Park. 800 singers and musicians took part in the Festival. Huge number of artisans and members of Self Help Groups also participated.</p>
                            <h4>Bangla Moder Gorbo:</h4>
                            <p>Information & Cultural Affairs Department organized a 3-day long programme in all 22 districts between November, 2022 & January, 2023 under the banner head ‘Bangla Moder Gorbo’ to spread the message of the developmental schemes undertaken by the present Government so that the common people can avail it to the maximum. Artistes, both from Kolkata and local areas along with the LPP folk artistes, took part in the programmes. An exhibition titled ‘Muktir Sangrame Bangla’ was also displayed at each of the venues; which were highly appreciated by people. Participation from all walks of life made this festival a grand success and provided huge scope to a large number of folk artistes to stage their folk genres.</p>
                            <h4>Paschim Banga Charukala Utsav:</h4>
                            <p>Paschim Banga Charukala Utsav-2022 has been organised from 22nd November 2022 to 28th November 2022, by Rajya Charukala Parshad at Nandan-Rabindra Sadan Complex and Gaganendra Shilpa Pradarshashala. More than 500 artistes participated in the Charukala Utsav.</p>
                            <h4>21stNatya Mela:</h4>
                            <p>The Paschim Banga Natya Academy has organized 22nd Natya Mela from 16th February 2023 to 26th February, 2023 at Rabindra Sadan, Sisir Mancha, Madhusudan Mancha, Girish Mancha, Minerva Theatre, Rabindra Tirtha Newtown, Tripti Mitra Natyagriha and at Natya Academy Open Air Theatre.</p>
                            <h4>Sahitya Utsav-O-Little Magazine Mela:</h4>
                            <p>Paschimbanga Bangla Academy organized ‘Sahitya Utsav-O-Little Magazine Mela’ from 11th January 2023 to 15th January 2023 at Rabindra Sadan-Nandan Campus. 350 nos. stalls were made and more than 500 poets, editors, literary persons had participated in this festival from all over the State.</p>
                           
