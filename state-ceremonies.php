                            <h2><strong>State Ceremonies, other ceremonies and observance of Birth & Death anniversaries - </strong></h2>
                            <h4>Republic Day Celebration & Observance of Independence Day:</h4>
                            <p>Republic Day and Independence Day was observed solemnly at Red Road, Kolkata along with District and Sub-Divisions.</p>
                            <h4>International Mother Language Day:</h4>
                            <p>International Mother Language Day was observed at Deshopriyo Park, Kolkata and different districts of the State on 21st February 2023. Eminent Artistes and general public participated in these programmes spontaneously.</p>
                            <h4>Martyr's Day:</h4>
                            <p>Martyr's Day was observed on 30thJanuary, 2023 to pay tribute to Mahatma Gandhi on his death anniversary in solemn manner in all districts and sub-divisions of the state. The State Level programme was organised at Gandhi Ghat, Barrackpore. Floral tributes were also paid at the Statue of Mahatma Gandhi at Mayo Road, Kolkata.</p>
                            <h4>Observance of birth and death Anniversaries of Rabindranath Tagore:</h4>
                            <p>The Department had observed ‘Pochisey Baisakh’ on 9th May 2022 by way of garlanding the statue and portrait of Rabindranath Tagore at Cathedral Road and Rabindra Sadan Campus, followed by presentation of Rabindra Sangeet by eminent artistes through online programme. The day was also observed in all districts and sub-divisions. Rabindra Smriti Puraskar were awarded to eminent literary personalities on this day. Thereafter, ‘Kabi Pakshya’ also held from 10th May 2022 to 24th May 2022 at Rabindra Sadan, Sisir Mancha, Ektara Mukta Mancha and Bangla Academy Sabhagriha.</p>
                            <p>‘Baishe Shraban’ was also commemorated as death anniversary of Rabindranath Tagore in Kolkata and all other districts and sub-divisions to pay homage to the Kabiguru.</p>
                            <h4>Observance of Birth & Death Anniversary of Kazi Nazrul Islam:</h4>
                            <p>The birth anniversary was observed on 26th May’22 at Nazrul Tirtha, New Town, Kolkata and in all districts & Sub-divisions. Thereafter, ‘Nazrul Janmotsov’ was also held from 27th May 2022 to 29th May 2022 at Rabindra Sadan, Sisir Mancha, Ektara Mukta Mancha and Bangla Academy Sabhagriha.</p>
